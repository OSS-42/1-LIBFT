int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	if (nb == 0)
		return (0);
	while (x * x <= nb && x < 46341)
	{
		if (x * x == nb)
			return (x);
		x++;
	}
	return (0);
}
